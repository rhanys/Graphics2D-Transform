    public void CriaGeometria() throws IOException
    {
        System.out.println("criando");
       principal.getContentPane().add(painel);
        principal.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        principal.setSize(1000, 600);
        principal.setVisible(true);
       

            fieldX = this.jTextFieldX.getText();
            fieldY = this.jTextFieldY.getText();
        
       /*
        else
        {
            fieldX = fieldX + " " + this.jTextFieldX.getText();
            fieldY = fieldY + " " + this.jTextFieldY.getText();
            
        }*/
        fieldId = this.jTextFieldId.getText();
         System.out.println(fieldX);
        System.out.println(fieldY);
        // float[] testX = new float[]{10f, 20f, 20f, 0f};
       // float[] testY = new float[]{10f, 20f, 20f, 0f};
       String arrayStringX[], arrayStringY[];
       arrayStringX= fieldX.split(" ");
       arrayStringY= fieldY.split(" ");
       float[] fX = new float[arrayStringX.length];
       float[] fY = new float[arrayStringY.length];

       
       for(int i=0; i<arrayStringX.length; i++)
       {
           fX[i] = Float.parseFloat(arrayStringX[i]);

       }
       
         for(int i=0; i<arrayStringY.length; i++)
       {
            fY[i] = Float.parseFloat(arrayStringY[i]);
       }
       
       Geometria geometria = Geometria.build(fX, fY, fieldId);
        painel.addGeometria(geometria);
        principal.setVisible(true);
        
        
    }

