    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       Geometria geometria = null;
        int sizeG = painel.getGeometrias().size();
        int comboBoxTransformValue = this.jComboBoxTransform.getSelectedIndex();
        
         for(int i = 0; i<sizeG; i++)
                       {
                         String comp1 = painel.getGeometrias().get(i).getId();
                         String comp2 = this.jTextFieldGeometria.getText();

                           if(comp1.equals(comp2))
                           {

                               geometria = painel.getGeometrias().get(i);
                               
                           }
                       }
         painel.setDx(Float.parseFloat(this.jTextFieldAngulo.getText()));
         
         if(this.jTextFieldDy.getText() != "")
         painel.setDy(Float.parseFloat(this.jTextFieldDy.getText()));
         
         else
             painel.setDy(1f);

    
        
       switch(comboBoxTransformValue)
       {
           case 0: //rot
                        painel.aplicaRotacao(geometria);
               break;
           case 1: //transl
                 
                        painel.aplicaTranslacao(geometria);
               break;
           case 2: //escala
                        painel.aplicaEscala(geometria);
               break;
       }
        
       
    }//GEN-LAST:event_jButton2ActionPerformed

